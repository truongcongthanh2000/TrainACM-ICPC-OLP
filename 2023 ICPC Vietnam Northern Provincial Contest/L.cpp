vector<int> phi_1_to_n(int n) {
    vector<int> phi(n + 1);
    for (int i = 0; i <= n; i++)
        phi[i] = i;

    for (int i = 2; i <= n; i++) {
        if (phi[i] == i) {
            for (int j = i; j <= n; j += i)
                phi[j] -= phi[j] / i;
        }
    }
    return phi;
}

// phi(a * b) = phi(a) * phi(b) * d / phi(d)
void sol() {
    vector<int> phi = phi_1_to_n(5 * 1000000);
    int a, b;
    cin >> a >> b;
    int ans = 0;
    for (int i = 1; i <= b; i++) {
        int gcd = __gcd(i, a);
        __uint128_t value = 1LL * phi[a] * phi[i] / phi[gcd];
        value = value * gcd;
        value %= MOD;
        ans = (ans + value) % MOD;
    }
    cout << ans << '\n';
}

